# Python 转义字符示例
# 转义字符用于表示一些不能直接显示的 ASCII 字符

# 1. 反斜杠（\）
print("1.反斜杠  这是一个反斜杠：\\")  # 输出: 这是一个反斜杠：\

# 2. 单引号（'）
print('2.单引号 字符串中的单引号：\'')  # 输出: 字符串中的单引号：'

# 3. 双引号（"）
print("3.双引号 字符串中的双引号：\"")  # 输出: 字符串中的双引号："

# 4. 响铃（BEL）- 发出系统提示音
print("4.响铃 \a")  # 输出: （会发出“嘀”一声）

# 5. 退格符（BS）- 删除前一个字符
print("5.退格符 Hello\bWorld")  # 输出: HellWorld（删除了 'o'）

# 6. 换行符（LF）- 换行
print("6.换行符 第一行\n第二行")  # 输出:
# 第一行
# 第二行

# 7. 水平制表符（TAB）- 缩进
print("7.水平制表符 列1\t列2\t列3")  # 输出: 列1    列2    列3

# 8. 垂直制表符（VT）- 垂直制表（较少使用）
print("8.垂直制表符 行1\v行2")  # 输出: 行1（垂直空格）行2

# 9. 回车符（CR）- 光标回到行首
print("9.回车符 Hello\rWorld")  # 输出: World（覆盖前面的内容）

# 10. 换页符（FF）- 页面换页（通常无可见效果）
print("10.换页符 第一页\f第二页")  # 输出: 第一页第二页（可能无明显变化）

# 11. 八进制转义（\ooo）
# ooo 是 0-7 的八进制数，例如 \141 = 'a'
print("11.八进制转义 \141")  # 输出: a（因为 141 是八进制的 'a'）

# 12. 十六进制转义（\xhh）
# hh 是两位十六进制数，例如 \x61 = 'a'
print("12.十六进制转义 \x61")  # 输出: a（因为 61 是十六进制的 'a'）

# 综合示例：使用多个转义字符
message = "使用多个转义字符   路径是：C:\\Users\\name\\Documents\n注意：\t不要忘记备份！"
print(message)
# 输出：
# 路径是：C:\Users\name\Documents
# 注意：	不要忘记备份！

# 提示：在字符串中使用反斜杠时，若不想转义，可使用原始字符串 r""
raw_path = r"使用 r\"\"避免转义  C:\Users\name\Documents"  # 使用 r"" 避免转义
print(raw_path)  # 输出: C:\Users\name\Documents